// mergeEnv overlays add onto base; if a key exists in both, add wins.
func mergeEnv(base []string, add map[string]string) []string {
	if len(add) == 0 {
		return base
	}
	idx := make(map[string]int, len(base))
	for i, kv := range base {
		for j := 0; j < len(kv); j++ {
			if kv[j] == '=' {
				idx[kv[:j]] = i
				break
			}
		}
	}
	out := append([]string(nil), base...)
	for k, v := range add {
		entry := k + "=" + v
		if i, ok := idx[k]; ok {
			out[i] = entry
		} else {
			out = append(out, entry)
		}
	}
	return out
}
